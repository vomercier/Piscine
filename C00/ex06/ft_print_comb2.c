#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	a[2];
	char	b[2];

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			a[0] = '0' + i / 10;
			a[1] = '0' + i % 10;
			b[0] = '0' + j / 10;
			b[1] = '0' + j % 10;
			write(1, &a, 2);
			write(1, " ", 1);
			write(1, &b, 2);
			if (!(i == 98 && j == 99))
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
}
